public class Sample {
  public static void main(string[] args) {
    int num = 3;
    String menu = "";
    
    switch (num) {
      case 1 : menu = "espresso"; break;
      case 2 : menu = "americano"; break;
      case 3 : menu = "latte"; break;
    }
    
    System.out.println(menu);
    
  }
}
